module CalculatorService
  def multiple (first_num,last_num)
    first_num*last_num
  end

  def subtract(first_num,last_num)
    first_num-last_num
  end

  def divide(first_num,last_num)
    first_num/last_num
  end

  def add(first_num,last_num)
    first_num+last_num
  end

  def mode(first_num,last_num)
    first_num%last_num
  end

end
